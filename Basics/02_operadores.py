# Operadores Logicos / Logica booleana

print(3 > 4 and "Hola" > "Python") # 'Y' o && como se conoce en otros lenguajes
print(3 > 4 or "Hola" > "Python") # 'O' o || como se conoce en otros lenguajes

print(3 < 4 and "Hola" < "Python")  
print(3 < 4 or "Hola" > "Python") 
print(3 < 4 or "Hola" > "Python" and 4 == 4)
print(not(3 > 4)) # Esto deberia devolvernos False pero cuando usamos el 'not' revertimos el valor a True










