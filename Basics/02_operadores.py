# Operadores

print(8 + 2)
print(8 - 2)
print(8 / 2)
print(8 * 2)
print(2 ** 3) # Exponencial
print(2 ** 3 + 3 - 7 / 1 // 4)

# Esta es la division aproximada o truncada funciona como trunc()
print(10 // 3)
 
# Operador de modulo, nos devuelve el resto = 0 o 1
print(8 % 2)

# Tambien podemos sumar cadenas de texto / concatenar cadenas
print("Hola" + "Python")
print("Hola " + str(5))
print("Hola " * 5)

my_float = 2.5 * 2
print("Hola" * int(my_float))

